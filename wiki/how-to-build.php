            <div class="step-item">
                <div class="step-number">1</div>
                <div class="step-content">
                    <h3>Prepare Your Workspace</h3>
                    <div class="step-details">
                        <h4>What you need:</h4>
                        <ul>
                            <li>Clean, well-lit workspace</li>
                            <li>Phillips head screwdriver</li>
                            <li>Anti-static wrist strap (recommended)</li>
                            <li>Small container for screws</li>
                            <li>All your components</li>
                        </ul>
                        
                        <h4>Safety Tips:</h4>
                        <ul>
                            <li>Work on a non-carpeted surface</li>
                            <li>Touch metal parts to discharge static</li>
                            <li>Keep components in anti-static bags until needed</li>
                            <li>Don't force anything - if it doesn't fit, check orientation</li>
                        </ul>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">2</div>
                <div class="step-content">
                    <h3>Install the Power Supply</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Remove the side panel from your case</li>
                            <li>Locate the PSU mounting area (usually at the bottom)</li>
                            <li>Insert the power supply with the fan facing down (if there's a vent) or up</li>
                            <li>Secure with the provided screws</li>
                            <li>Route the power cables through the back of the case</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> Don't connect any cables yet - we'll do that later when installing components.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">3</div>
                <div class="step-content">
                    <h3>Install the CPU</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Open the CPU socket lever on the motherboard</li>
                            <li>Carefully remove the CPU from its packaging</li>
                            <li>Align the CPU with the socket (look for the golden triangle)</li>
                            <li>Gently place the CPU in the socket - it should drop in easily</li>
                            <li>Close the lever to secure the CPU</li>
                        </ol>
                        
                        <div class="step-warning">
                            <strong>‚ö†Ô∏è Warning:</strong> Never force the CPU into the socket. If it doesn't fit, check the orientation.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">4</div>
                <div class="step-content">
                    <h3>Install the CPU Cooler</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Apply thermal paste (if not pre-applied)</li>
                            <li>Place the cooler over the CPU</li>
                            <li>Secure with the provided mounting brackets</li>
                            <li>Connect the cooler's power cable to the CPU_FAN header</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> If using an aftermarket cooler, check the manual for specific mounting instructions.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">5</div>
                <div class="step-content">
                    <h3>Install RAM</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Open the RAM slot clips</li>
                            <li>Align the RAM stick with the slot (notch should match)</li>
                            <li>Press down firmly until the clips snap into place</li>
                            <li>Repeat for additional RAM sticks</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> Install RAM in the slots recommended by your motherboard manual for optimal performance.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">6</div>
                <div class="step-content">
                    <h3>Install the Motherboard</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Install the I/O shield in the case</li>
                            <li>Install motherboard standoffs in the case</li>
                            <li>Carefully place the motherboard in the case</li>
                            <li>Secure with screws (don't overtighten)</li>
                            <li>Connect the front panel cables (power, reset, LEDs)</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> The front panel cables are the trickiest part. Refer to your motherboard manual for the exact pin layout.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">7</div>
                <div class="step-content">
                    <h3>Install Storage Drives</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>For SSDs: Mount in 2.5" drive bays or use adapter brackets</li>
                            <li>For HDDs: Mount in 3.5" drive bays</li>
                            <li>Connect SATA data cables to motherboard</li>
                            <li>Connect SATA power cables from PSU</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> NVMe SSDs install directly into the motherboard - no cables needed!
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">8</div>
                <div class="step-content">
                    <h3>Install the Graphics Card</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Remove the appropriate PCIe slot covers from the case</li>
                            <li>Insert the GPU into the PCIe x16 slot</li>
                            <li>Secure with screws to the case</li>
                            <li>Connect PCIe power cables from the PSU</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> Make sure the GPU is fully seated - you should hear a click when it's properly installed.
                        </div>
                    </div>
                </div>
            </div>

            <div class="step-item">
                <div class="step-number">9</div>
                <div class="step-content">
                    <h3>Connect Power Cables</h3>
                    <div class="step-details">
                        <h4>Steps:</h4>
                        <ol>
                            <li>Connect 24-pin motherboard power cable</li>
                            <li>Connect 8-pin CPU power cable</li>
                            <li>Connect PCIe power cables to GPU</li>
                            <li>Connect SATA power cables to drives</li>
                            <li>Route cables neatly behind the motherboard tray</li>
                        </ol>
                        
                        <div class="step-tip">
                            <strong>üí° Tip:</strong> Use cable ties to keep everything organized and improve airflow.
                        </div>
                    </div>
                </div>
            </div>

